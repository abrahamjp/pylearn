#Protocls in python are to interfaces to Java\C#.
#If a type supports a protocol, it must implement the methods specific by the  protocol
#Example: 
#Container(str,list,range,tuple,bytesi,set,dict)  this protocol helps with using "in" and "not in" operators
#Sized(str,list,range,tuple,bytesi,set,dict)  this is used for len() operations
#Iterable(str,list,range,tuple,bytesi,set,dict)  used in for loops
#Sequence(str,list,range,tuple,bytes)
#Mutable Sequence(list)
#Mutable Set(set)
#Mutable Mapping(dict)

#unlike other langs, implementing protocols in python doesnt require declaring base methods in a different file

